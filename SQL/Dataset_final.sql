CREATE TABLE IMPAYE_FEATURES_YEAR AS
SELECT 
    NUM_AFF,
    ANNEE,

    SUM(CASE WHEN PER_MOIS >= ADD_MONTHS(MAX_MOIS, -2) AND FLAG_CLEAN = 0 THEN 1 ELSE 0 END) AS NB_IMPAYE_3M,

    SUM(CASE WHEN PER_MOIS >= ADD_MONTHS(MAX_MOIS, -5) AND FLAG_CLEAN = 0 THEN 1 ELSE 0 END) AS NB_IMPAYE_6M,

    SUM(CASE WHEN PER_MOIS >= ADD_MONTHS(MAX_MOIS, -11) AND FLAG_CLEAN = 0 THEN 1 ELSE 0 END) AS NB_IMPAYE_12M

FROM (
    SELECT 
        NUM_AFF,
        FLAG_CLEAN,
        EXTRACT(YEAR FROM TO_DATE(TO_CHAR(PERIODE),'YYYYMM')) AS ANNEE,

        TRUNC(TO_DATE(TO_CHAR(PERIODE),'YYYYMM'),'MM') AS PER_MOIS,

        MAX(TRUNC(TO_DATE(TO_CHAR(PERIODE),'YYYYMM'),'MM')) 
            OVER (PARTITION BY NUM_AFF, EXTRACT(YEAR FROM TO_DATE(TO_CHAR(PERIODE),'YYYYMM'))) AS MAX_MOIS
    FROM FAIT_PAYEMENT
)
GROUP BY NUM_AFF, ANNEE;


