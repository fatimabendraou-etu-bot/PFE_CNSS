CREATE TABLE DATASET_FEATURES AS
SELECT 
    d_curr.NUM_AFF,
    d_curr.ANNEE,

    -- Features paiement
    ROUND(d_curr.TOTAL_DEBIT,4)        AS TOTAL_DEBIT,
    ROUND(d_curr.MONTANT_IMPAYE,4)     AS MONTANT_IMPAYE,
    ROUND(d_curr.TOTAL_PENALITE,4)     AS TOTAL_PENALITE,
    ROUND(d_curr.TAUX_RECOUVREMENT,4)  AS TAUX_RECOUVREMENT,
    ROUND(d_curr.TAUX_IMPAYE,4)        AS TAUX_IMPAYE,
    d_curr.FLAG_PENALITE,
    d_curr.NB_IMPAYE_3M,
    d_curr.NB_IMPAYE_6M,
    d_curr.NB_IMPAYE_12M,

    -- Features salaire
    ROUND(s.SALAIRE_BRUTE_AVG,4)       AS SALAIRE_BRUTE_AVG,
    ROUND(s.SALAIRE_BRUTE_MAX,4)       AS SALAIRE_BRUTE_MAX,
    ROUND(s.SALAIRE_BRUTE_MIN,4)       AS SALAIRE_BRUTE_MIN,

    -- Features affilié
    NVL(n.NB_IMMA,0)                   AS NB_IMMA,
    d_dim.AGE_ENTREPRISE,
    d_dim.SECTEUR_ID,
    d_dim.LIBELLE_SECTEUR,
    d_dim.REGION_ID,
    d_dim.LIBELLE_REGION,
    d_dim.VILLE_ID,
    d_dim.LIBELLE_VILLE

FROM FEATURES_PAYEMENT d_curr

LEFT JOIN SAL_STAT s 
    ON d_curr.NUM_AFF = s.NUM_AFF 
   AND d_curr.ANNEE   = s.ANNEE

LEFT JOIN NB_IMMA n 
    ON d_curr.NUM_AFF = n.NUM_AFF 
   AND d_curr.ANNEE   = n.ANNEE

LEFT JOIN FEATURES_DIM_AFFILIE d_dim
    ON d_curr.NUM_AFF = d_dim.NUM_AFF

WHERE d_curr.ANNEE IN (2022, 2023);

COMMIT;
CREATE TABLE DATASET_TARGET AS
SELECT 
    NUM_AFF,
    ANNEE,

    CASE 
        WHEN NVL(SUM(TOTAL_DEBIT),0) = 0 THEN 0

        WHEN (
            SUM(NVL(TOTAL_CREDIT,0)) 
            / NULLIF(SUM(NVL(TOTAL_DEBIT,0)),0)
        ) >= 0.99
        AND SUM(NVL(TOTAL_PENALITE,0)) <= 0
        THEN 1

        ELSE 0
    END AS TARGET_FLAG_CLEAN,

    CASE
        WHEN AVG(TAUX_RECOUVREMENT) < 0.50 THEN 'FAIBLE'
        WHEN AVG(TAUX_RECOUVREMENT) < 0.95 THEN 'MOYEN'
        ELSE 'ELEVE'
    END AS TARGET_RECOUVREMENT,


    ROUND(
        (SUM(MONTANT_IMPAYE) 
        / NULLIF(SUM(TOTAL_DEBIT),0)) * 100
    , 2) AS TARGET_POURCENTAGE_IMPAYE

FROM FEATURES_PAYEMENT

WHERE ANNEE = 2024

GROUP BY NUM_AFF, ANNEE;

COMMIT;

