CREATE TABLE DATASET_FEATURES_WIDE AS
SELECT *
FROM (
    SELECT 
        f.NUM_AFF,
        f.ANNEE,

        f.TOTAL_DEBIT,
        p.TOTAL_CREDIT,
        f.MONTANT_IMPAYE,
        f.TOTAL_PENALITE,
        f.TAUX_RECOUVREMENT,
        f.TAUX_IMPAYE,
        f.NB_IMPAYE_3M,
        f.NB_IMPAYE_6M,
        f.NB_IMPAYE_12M,
        f.SALAIRE_BRUTE_AVG,

        f.NB_IMMA,
        f.AGE_ENTREPRISE,
        f.SECTEUR_ID,
        f.REGION_ID,
        f.VILLE_ID,
        f.LIBELLE_SECTEUR,
        f.LIBELLE_REGION,
        f.LIBELLE_VILLE

    FROM DATASET_FEATURES f
    LEFT JOIN FEATURES_PAYEMENT p
        ON f.NUM_AFF = p.NUM_AFF
       AND f.ANNEE = p.ANNEE
)
PIVOT (
    MAX(TOTAL_DEBIT) AS TOTAL_DEBIT,
    MAX(TOTAL_CREDIT) AS TOTAL_CREDIT,
    MAX(MONTANT_IMPAYE) AS MONTANT_IMPAYE,
    MAX(TOTAL_PENALITE) AS TOTAL_PENALITE,
    MAX(TAUX_RECOUVREMENT) AS TAUX_RECOUVREMENT,
    MAX(TAUX_IMPAYE) AS TAUX_IMPAYE,
    MAX(NB_IMPAYE_3M) AS NB_IMPAYE_3M,
    MAX(NB_IMPAYE_6M) AS NB_IMPAYE_6M,
    MAX(NB_IMPAYE_12M) AS NB_IMPAYE_12M,
    MAX(SALAIRE_BRUTE_AVG) AS SALAIRE_AVG
    FOR ANNEE IN (2022 AS "2022", 2023 AS "2023")
);


