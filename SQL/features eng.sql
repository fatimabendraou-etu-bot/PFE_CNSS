--features engineering
CREATE TABLE FEATURES_PAYEMENT AS

WITH base AS (
    SELECT 
        NUM_AFF,
        PERIODE,
        SUBSTR(PERIODE,1,4) AS ANNEE,
        TO_DATE(PERIODE || '01','YYYYMMDD') AS PER_DATE,

        DEBIT_CNSS,
        DEBIT_AMO,
        DEBIT_TPF,
        CREDIT_CNSS,
        CREDIT_TPF,

        PN_CNSS,
        PN_TPF

    FROM FAIT_PAYEMENT
),

/* max date par affilié + année */
year_max AS (
    SELECT 
        NUM_AFF,
        SUBSTR(PERIODE,1,4) AS ANNEE,
        MAX(TO_DATE(PERIODE || '01','YYYYMMDD')) AS MAX_DATE_YEAR
    FROM FAIT_PAYEMENT
    GROUP BY NUM_AFF, SUBSTR(PERIODE,1,4)
)

SELECT 
    b.NUM_AFF,
    b.ANNEE,

    SUM(NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0)) AS TOTAL_DEBIT,

    SUM(NVL(b.CREDIT_CNSS,0) + NVL(b.CREDIT_TPF,0)) AS TOTAL_CREDIT,

    GREATEST(
        SUM(NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0))
        -
        SUM(NVL(b.CREDIT_CNSS,0) + NVL(b.CREDIT_TPF,0))
    ,0) AS MONTANT_IMPAYE,

    SUM(NVL(b.PN_CNSS,0) + NVL(b.PN_TPF,0)) AS TOTAL_PENALITE,

    CASE 
        WHEN SUM(NVL(b.PN_CNSS,0) + NVL(b.PN_TPF,0)) > 0 THEN 1
        ELSE 0
    END AS FLAG_PENALITE,

    ROUND(
        CASE 
            WHEN SUM(NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0)) = 0 
            THEN 0
            ELSE 
                SUM(NVL(b.CREDIT_CNSS,0) + NVL(b.CREDIT_TPF,0)) /
                SUM(NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0))
        END
    ,4) AS TAUX_RECOUVREMENT,

    ROUND(
        CASE 
            WHEN SUM(NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0)) = 0 
            THEN 0
            ELSE 
                1 -
                (
                    SUM(NVL(b.CREDIT_CNSS,0) + NVL(b.CREDIT_TPF,0)) /
                    SUM(NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0))
                )
        END
    ,4) AS TAUX_IMPAYE,

    CASE 
        WHEN SUM(NVL(b.CREDIT_CNSS,0) + NVL(b.CREDIT_TPF,0)) /
             NULLIF(SUM(NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0)),0) >= 0.98
        THEN 1 ELSE 0
    END AS FLAG_CLEAN,

    SUM(
        CASE 
            WHEN (NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0))
               > (NVL(b.CREDIT_CNSS,0) + NVL(b.CREDIT_TPF,0))
            THEN 1 ELSE 0
        END
    ) AS NB_IMPAYE_12M,

    SUM(
        CASE 
            WHEN b.PER_DATE >= ADD_MONTHS(y.MAX_DATE_YEAR, -3)
             AND (NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0))
               > (NVL(b.CREDIT_CNSS,0) + NVL(b.CREDIT_TPF,0))
            THEN 1 ELSE 0
        END
    ) AS NB_IMPAYE_3M,

    SUM(
        CASE 
            WHEN b.PER_DATE >= ADD_MONTHS(y.MAX_DATE_YEAR, -6)
             AND (NVL(b.DEBIT_CNSS,0) + NVL(b.DEBIT_AMO,0) + NVL(b.DEBIT_TPF,0))
               > (NVL(b.CREDIT_CNSS,0) + NVL(b.CREDIT_TPF,0))
            THEN 1 ELSE 0
        END
    ) AS NB_IMPAYE_6M

FROM base b

JOIN year_max y
  ON b.NUM_AFF = y.NUM_AFF
 AND b.ANNEE = y.ANNEE

GROUP BY 
    b.NUM_AFF,
    b.ANNEE;

CREATE TABLE FEATURES_DIM_AFFILIE AS
SELECT 

    a.NUM_AFF,
    a.DATE_AFF,
    ROUND((SYSDATE - a.DATE_AFF)/365, 2) AS AGE_ENTREPRISE,
    a.SECTEUR_ID,
    s.LIBELLE_SECTEUR,
    a.REGION_ID,
    r.LIBELLE_REGION,
    a.VILLE_ID,
    v.LIBELLE_VILLE
    
FROM DIM_AFFILIE a
LEFT JOIN DIM_SECTEUR s ON a.SECTEUR_ID = s.SECTEUR_ID
LEFT JOIN DIM_REGION r ON a.REGION_ID = r.REGION_ID
LEFT JOIN DIM_VILLE v ON a.VILLE_ID = v.VILLE_ID;






CREATE TABLE SAL_STAT AS
SELECT 
    NUM_AFF,
    EXTRACT(YEAR FROM PERIODE) AS ANNEE,
    AVG(SALAIRE_BRUTE) AS SALAIRE_BRUTE_AVG,
    MAX(SALAIRE_BRUTE) AS SALAIRE_BRUTE_MAX,
    MIN(SALAIRE_BRUTE) AS SALAIRE_BRUTE_MIN
FROM FAIT_SALAIRE
WHERE EXTRACT(YEAR FROM PERIODE) BETWEEN 2022 AND 2024
GROUP BY NUM_AFF, EXTRACT(YEAR FROM PERIODE);

UPDATE SAL_STAT
SET SALAIRE_BRUTE_AVG = ROUND(SALAIRE_BRUTE_AVG, 4);


CREATE TABLE NB_IMMA AS
SELECT 
    NUM_AFF,
    EXTRACT(YEAR FROM PERIODE) AS ANNEE,
    COUNT(DISTINCT NUM_IMMA) AS NB_IMMA
FROM FAIT_SALAIRE
WHERE EXTRACT(YEAR FROM PERIODE) BETWEEN 2022 AND 2024
GROUP BY NUM_AFF, EXTRACT(YEAR FROM PERIODE);

