CREATE TABLE FEATURES_IMMATRICULE AS
SELECT 
    f.NUM_AFF,
    f.ANNEE,
    s.SALAIRE_BRUTE_AVG,
    s.SALAIRE_BRUTE_MAX,
    s.SALAIRE_BRUTE_MIN,
    NVL(n.NB_IMMA, 0) AS NB_IMMA, 
    t.TARGET_CLASS
FROM FEATURES_PAYEMENT f
LEFT JOIN SAL_STAT s ON f.NUM_AFF = s.NUM_AFF AND f.ANNEE = s.ANNEE
LEFT JOIN NB_IMMA n ON f.NUM_AFF = n.NUM_AFF AND f.ANNEE = n.ANNEE
LEFT JOIN TARGET_AFF t ON f.NUM_AFF = t.NUM_AFF AND f.ANNEE = t.ANNEE;



    


    




